function open_login(){
    var form_entrar = document.getElementById('form-login');
    var form_cadastro = document.getElementById('form-cadastro');
    form_entrar.style.display = 'inline';
    form_cadastro.style.display = 'none';
}
function open_cadastro(){
    var form_entrar = document.getElementById('form-login');
    var form_cadastro = document.getElementById('form-cadastro');
    form_entrar.style.display = 'none';
    form_cadastro.style.display = 'inline';
}
